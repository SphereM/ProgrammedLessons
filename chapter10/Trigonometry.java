public class Trigonometry 
{
    public static void main(String[] args) 
    {
        double sinx = 0.5236;
        double cosx = 0.5236;
        double sum;
        sinx = Math.sin(sinx);
        cosx = Math.cos(cosx);
        sum = sinx + cosx;
        System.out.println("sine: "+sinx+" cosine: "+cosx+" sum of squares: "+sum);
    }
}